declare(strict_types=1);

namespace Magefan\GoogleShoppingFeed\Block\Adminhtml\System\Config\Form;

use Magefan\GoogleShoppingFeed\Model\Config\Source\GoogleAttributes;
use Magento\Framework\View\Element\Html\Select;

class GoogleTagColumn extends Select
{
    /**
     * Set "name" for <select> element
     *
     * @param string $value
     * @return $this
     */
    public function setInputName($value)
    {
        return $this->setName($value);
    }

    /**
     * Set "id" for <select> element
     *
     * @param $value
     * @return $this
     */
    public function setInputId($value)
    {
        return $this->setId($value);
    }

    /**
     * Render block HTML
     *
     * @return string
     */
    public function _toHtml(): string
    {
        if (!$this->getOptions()) {
            $this->setOptions($this->getSourceOptions());
        }
        return parent::_toHtml();
    }

    private function getSourceOptions(): array
    {
        $attrs = [];
        foreach (GoogleAttributes::attributes as $attr) {
                $attrs[] = [
                    'label' => $attr['label'],
                    'value' => $attr['value']
                ];
        }

        return $attrs;
    }
}
